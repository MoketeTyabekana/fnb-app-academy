# Operators on Numeric Data

# addition(+)
# subtraction(-)
# multiplication(*)
# division(/)
# floor division(//)
# modulus(%)
# exponentiation(**)

x=10
y=5

print(x+y)  # addition
print(x-y)  # subtraction
print(x*y)  # multiplication
print(x/y)  # division  
print(x//y) # floor division
print(x%y)  # modulus
print(x**y) # exponentiation