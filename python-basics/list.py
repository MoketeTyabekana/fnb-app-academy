# Lists

fruits=["Banana","Apple","Cherry"]

print(fruits[0])

fruits[1]="Blueberry"
print(fruits[1])

# Removing Items from the list.
fruits.remove(fruits[0])
print(fruits)

# Adding a new item into the list.
fruits.append("Kiwi")
print(fruits)


# Adding new item into a specific position.

fruits.insert(0,"Orange")
print(fruits)